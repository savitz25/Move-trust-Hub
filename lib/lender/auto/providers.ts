import type { AutoLoanProvider } from './types';

function makeProvider(
  base: Omit<AutoLoanProvider, 'id' | 'slug'> & { id: string }
): AutoLoanProvider {
  const slug = base.name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');
  return { ...base, slug };
}

