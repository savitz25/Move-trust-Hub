import type { Metadata } from 'next';

export const SITE_URL = 'https://www.movetrusthub.com';
export const SITE_NAME = 'Move Trust Hub';

export const HOMEPAGE_TITLE =
  'Compare FMCSA-Licensed Movers (2026) | Independent Moving Directory';
export const HOMEPAGE_DESCRIPTION =
  'Research FMCSA-licensed interstate movers by attributable reviews, pricing, and safety ratings. Free moving calculator, 480+ city guides, and 3,100+ county directories. Independent directory — no lead fees, no paid placements.';

export const DEFAULT_SITE_DESCRIPTION =
  'Compare FMCSA-licensed interstate movers by reviews, pricing, and reputation. Free moving calculator and trusted long-distance carrier research at Move Trust Hub — independent directory, no lead fees.';

/** 1200×630 — generated by app/opengraph-image.tsx (and hub-specific variants). */
export const OG_IMAGE = {
  url: '/opengraph-image',
  width: 1200,
  height: 630,
  alt: 'Move Trust Hub — independent directory for FMCSA-licensed interstate movers',
} as const;

export const LENDER_OG_IMAGE = {
  url: '/lender/opengraph-image',
  width: 1200,
  height: 630,
  alt: 'Lender Trust Hub — NMLS-verified mortgage lenders by county',
} as const;

export const INSURANCE_OG_IMAGE = {
  url: '/insurance/opengraph-image',
  width: 1200,
  height: 630,
  alt: 'Insurance Trust Hub — DOI-verified insurance agents and health hubs',
} as const;

export type OgHub = 'move' | 'lender' | 'insurance';

export function getOgImageForHub(hub: OgHub = 'move') {
  if (hub === 'lender') return LENDER_OG_IMAGE;
  if (hub === 'insurance') return INSURANCE_OG_IMAGE;
  return OG_IMAGE;
}

export function getSiteNameForHub(hub: OgHub = 'move') {
  if (hub === 'lender') return 'Lender Trust Hub';
  if (hub === 'insurance') return 'Insurance Trust Hub';
  return SITE_NAME;
}

export function buildOpenGraph(
  overrides: {
    title?: string;
    description?: string;
    url?: string;
    type?: 'website' | 'article';
    hub?: OgHub;
  } = {}
): NonNullable<Metadata['openGraph']> {
  const hub = overrides.hub ?? 'move';
  const ogImage = getOgImageForHub(hub);
  return {
    title: overrides.title ?? HOMEPAGE_TITLE,
    description: overrides.description ?? HOMEPAGE_DESCRIPTION,
    url: overrides.url ?? SITE_URL,
    siteName: getSiteNameForHub(hub),
    type: overrides.type ?? 'website',
    locale: 'en_US',
    images: [ogImage],
  };
}

export function buildTwitter(
  overrides: {
    title?: string;
    description?: string;
    hub?: OgHub;
  } = {}
): NonNullable<Metadata['twitter']> {
  const hub = overrides.hub ?? 'move';
  const ogImage = getOgImageForHub(hub);
  return {
    card: 'summary_large_image',
    title: overrides.title ?? HOMEPAGE_TITLE,
    description: overrides.description ?? HOMEPAGE_DESCRIPTION,
    images: [ogImage.url],
  };
}

