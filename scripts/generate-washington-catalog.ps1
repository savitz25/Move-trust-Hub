$outDir = Join-Path $PSScriptRoot 'washington-output'
New-Item -ItemType Directory -Force -Path $outDir | Out-Null

function Quote-Ts([string]$s) {
  if ($s -match "'") { return '"' + $s + '"' }
  return "'" + $s + "'"
}

$counties = @(
  @{
    slug='king'; city='Seattle'; citySlug='seattle'; label='Seattle Metro'; poolId='king-metro-wa'
    regional1='seattle-corridor'; regional2='puget-sound'
