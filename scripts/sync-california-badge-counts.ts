/**
 * Sync californiaMarketMoverCounts to exact getMoversForCounty lengths.
 * Run after rebuild-california-county-movers.ts
 */
import { writeFileSync } from 'node:fs';
import { californiaCounties } from '../lib/local-movers/geography/california';
import { getMoversForCounty } from '../lib/local-movers/index';
import { californiaCountyMoverAssignments } from '../data/california-county-assignments';

const counts: Record<string, number> = {};
const mismatches: Array<{ slug: string; listed: number; assigned: number }> = [];

for (const county of californiaCounties) {
  const listed =
    getMoversForCounty('california', county.slug)?.movers.length ?? 0;
  const assigned =
    californiaCountyMoverAssignments.find((a) => a.countySlug === county.slug)
      ?.moverIds.length ?? 0;
  counts[county.slug] = listed;
  if (listed !== assigned) {
    mismatches.push({ slug: county.slug, listed, assigned });
  }
  console.log(
    `${county.slug.padEnd(18)} listed=${String(listed).padStart(2)} assigned=${String(assigned).padStart(2)}`
  );
}

if (mismatches.length) {
  console.error('Listed vs assigned mismatches:', mismatches);
  process.exitCode = 1;
}

const countLines = Object.keys(counts)
  .sort()
  .map((slug) => {
    const key = slug.includes('-') ? `'${slug}'` : slug;
    return `  ${key}: ${counts[slug]},`;
  })
  .join('\n');

const file = `/**
 * County mover badge counts for state directory grids.
 *
 * California values are the EXACT number of displayable movers on each
 * county page (getMoversForCounty). Regenerated by:
 *   npx tsx scripts/rebuild-california-county-movers.ts
 *   npx tsx scripts/sync-california-badge-counts.ts
 */

/** California — exact listed (displayable) movers per county page */
export const californiaMarketMoverCounts: Record<string, number> = {
${countLines}
};

const marketCountsByState: Record<string, Record<string, number>> = {
  california: californiaMarketMoverCounts,
};

/**
 * Listed/market count for a county, if we have a map.
 * Returns null when no map exists (caller may fall back to live listing count).
 */
export function getCountyMarketMoverCount(
  stateSlug: string,
  countySlug: string
): number | null {
  const map = marketCountsByState[stateSlug];
  if (!map) return null;
  const n = map[countySlug];
  return typeof n === 'number' ? n : null;
}

export type MoverCountBadgeTier = 'high' | 'medium' | 'low' | 'zero';

export function getMoverCountBadgeTier(count: number): MoverCountBadgeTier {
  if (count <= 0) return 'zero';
  if (count >= 50) return 'high';
  if (count >= 10) return 'medium';
  return 'low';
}

export function formatMoverCountLabel(count: number): string {
  if (count <= 0) return 'Contact Us';
  if (count === 1) return '1 Mover';
  return \`\${count} Movers\`;
}
`;

writeFileSync('lib/local-movers/county-market-mover-counts.ts', file);
console.log('\nWrote lib/local-movers/county-market-mover-counts.ts');
console.log(
  `Alameda=${counts.alameda}, Los Angeles=${counts['los-angeles']}, Alpine=${counts.alpine}`
);
