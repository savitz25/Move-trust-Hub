// Helper to get company by slug
export const getCompanyBySlug = (slug: string) => seedCompanies.find(c => c.slug === slug);

// Calculate reputation score helper (used in seed)
export const computeReputationScore = (c: Partial<Company>): number => {
  const ratingWeight = (c.overallRating || 0) * 15;
  const reviewWeight = Math.min((c.reviewCount || 0) / 120, 25);
  const complaintRatio = (c.fmcsaComplaints || 0) / Math.max((c.fmcsaShipments || 1), 1000);
  const complaintScore = Math.max(0, 25 - complaintRatio * 180);
  const bbbBonus = (c.bbbRating === 'A+' || c.bbbRating === 'A') ? 12 : (c.bbbRating?.startsWith('B') ? 6 : 0);
  const verifiedBonus = c.isVerified ? 8 : 0;
  const ageBonus = Math.min(((c.yearsInBusiness || 0) - 10) / 4, 10);
  return Math.min(100, Math.round(ratingWeight + reviewWeight + complaintScore + bbbBonus + verifiedBonus + ageBonus));
};
